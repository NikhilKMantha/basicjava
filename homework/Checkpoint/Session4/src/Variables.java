
public class Variables {

	public static void main(String[] args) {
		int age = 10;
		System.out.println(age);
		String name = "Nikhil";
		System.out.println(name);
		Double decimal = 3.5;
		System.out.println(decimal);
		char grade = 'A';
		System.out.println(grade + "+");
		boolean yes = true;
		if (yes == true) {
			System.out.println("yes");
		}
	}

}
